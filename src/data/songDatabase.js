// Search function with fuzzy matching
export const searchSongs = (query) => {
    if (!query || query.length < 2) return []

    const lowerQuery = query.toLowerCase()

    return songDatabase
        .filter(song => {
            const titleMatch = song.title.toLowerCase().includes(lowerQuery)
            const movieMatch = song.movie.toLowerCase().includes(lowerQuery)
            const artistMatch = song.artist.toLowerCase().includes(lowerQuery)
            return titleMatch || movieMatch || artistMatch
        })
        .slice(0, 8) // Return max 8 results
}

export default songDatabase
